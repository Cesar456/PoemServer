package cesar.poem.bean;

public class PoemTagDetail {
	private int id;
	private int poemID;
	private String tag;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPoemID() {
		return poemID;
	}
	public void setPoemID(int poemID) {
		this.poemID = poemID;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
}
